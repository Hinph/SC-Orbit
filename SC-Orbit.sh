#!/bin/sh
# SC-Orbit for MiSTer
# Release-candidate v2.2 multi-device architecture with controller-only tuning.
# No recovery folder, Quartus core, settings scripts, tester, or MiSTer.ini edits.

APP_NAME="SC-Orbit"
APP_VERSION="1.0"
ROOT_PREFIX="${SC_ORBIT_ROOT:-}"
NO_START="${SC_ORBIT_NO_START:-0}"
rp(){ printf '%s%s' "$ROOT_PREFIX" "$1"; }

FAT="$(rp /media/fat)"
SCRIPTS="$FAT/Scripts"
SELF_DEST="$SCRIPTS/SC-Orbit.sh"
LEGACY_SETTINGS_DIR="$SCRIPTS/SC-Orbit Settings"
APP="$FAT/linux/sc-orbit"
BIN="$APP/sc-orbit-driver.armv7l"
SOURCE="$APP/sc-orbit-driver.c"
START="$APP/start.sh"
STOP="$APP/stop.sh"
PID="$(rp /var/run/sc-orbit.pid)"
LOG="$(rp /tmp/sc-orbit.log)"
CFG="$APP/sc-orbit.ini"
OLD_CFG="$FAT/sc-orbit.ini"
PROFILE="$APP/sc-orbit-user-profile.ini"
OLD_PROFILE="$FAT/sc-orbit-user-profile.ini"
OLD_DIAG="$FAT/SC-Orbit-Diagnostic.txt"
OLD_TELEMETRY="$FAT/SC-Orbit-Release-Telemetry.txt"
OLD_SPINNER_TRACE="$FAT/SC-Orbit-Spinner-Trace.txt"
OLD_GYRO_TRACE="$FAT/SC-Orbit-Gyro-Trace.txt"
USER_STARTUP="$FAT/linux/user-startup.sh"
INITD="$(rp /etc/init.d)"
SELF="$0"

BIN_SIZE=67496
BIN_SHA="9195a42c6d04f4f969982cc0a40cba8c36b1fa92b0978b86334946c04f11f03a"

have(){ command -v "$1" >/dev/null 2>&1; }
decode(){ if have base64; then base64 -d; elif have busybox; then busybox base64 -d; else return 1; fi; }
valid(){
 f="$1"; sz="$2"; hs="$3"
 [ -f "$f" ] || return 1
 [ "$(wc -c <"$f" 2>/dev/null | tr -d ' ')" = "$sz" ] || return 1
 if have sha256sum; then [ "$(sha256sum "$f" 2>/dev/null | awk '{print $1}')" = "$hs" ] || return 1; fi
}
extract_block(){
 begin="$1"; end="$2"; target="$3"; sz="$4"; hs="$5"
 valid "$target" "$sz" "$hs" && return 0
 tmp="$target.tmp.$$"; rm -f "$tmp"
 sed -n "/^#$begin$/,/^#$end$/p" "$SELF" | sed '1d;$d;s/^#//' | decode >"$tmp" || { rm -f "$tmp"; return 1; }
 valid "$tmp" "$sz" "$hs" || { rm -f "$tmp"; return 1; }
 mv -f "$tmp" "$target"
}
queue_menu(){
 [ -z "$ROOT_PREFIX" ] || return 0
 (
   sleep 1
   [ -w /dev/MiSTer_cmd ] && printf 'load_core /media/fat/menu.rbf\n' > /dev/MiSTer_cmd 2>/dev/null || true
 ) >/dev/null 2>&1 &
}
finish(){ echo "$1"; sync 2>/dev/null || true; queue_menu; exit "${2:-0}"; }

stop_pidfile(){
 [ -s "$PID" ] || { rm -f "$PID"; return 0; }
 p="$(cat "$PID" 2>/dev/null)"
 if [ -n "$p" ] && kill -0 "$p" 2>/dev/null; then
   kill "$p" 2>/dev/null || true
   n=0
   while kill -0 "$p" 2>/dev/null && [ "$n" -lt 25 ]; do sleep 0.1; n=$((n+1)); done
   kill -0 "$p" 2>/dev/null && kill -9 "$p" 2>/dev/null || true
 fi
 rm -f "$PID"
}
kill_matching_processes(){
 hard="$1"
 for d in /proc/[0-9]*; do
   [ -d "$d" ] || continue
   p="${d#/proc/}"
   [ "$p" = "$$" ] && continue
   comm="$(cat "$d/comm" 2>/dev/null || true)"
   exe="$(readlink "$d/exe" 2>/dev/null || true)"
   cmd="$(tr '\000' ' ' <"$d/cmdline" 2>/dev/null || true)"
   sig="$(printf '%s %s %s' "$comm" "$exe" "$cmd" | tr '[:upper:]' '[:lower:]')"
   case "$sig" in
     *scwrapper*|*sc-wrapper*|*'/media/fat/linux/sc-orbit/'*|*sc-orbit-driver.armv7l*)
       if [ "$hard" = "1" ]; then kill -9 "$p" 2>/dev/null || true; else kill "$p" 2>/dev/null || true; fi
       ;;
   esac
 done
}
stop_old_processes(){
 [ -n "$ROOT_PREFIX" ] && return 0
 if have pidof; then
   for name in scwrapper SCWrapper sc-wrapper SC-Wrapper sc-orbit.armv7l sc-orbit-base.armv7l sc-orbit-io.armv7l sc-orbit-unified.armv7l sc-orbit-driver.armv7l; do
     for p in $(pidof "$name" 2>/dev/null); do kill "$p" 2>/dev/null || true; done
   done
 fi
 # Catch truncated, renamed, or differently-cased legacy SCWrapper processes,
 # plus every older SC-Orbit binary path. Closing them releases evdev grabs.
 kill_matching_processes 0
 sleep 1
 kill_matching_processes 1
 sleep 2
}
disable_legacy_services(){
 for svc in "$INITD"/S*scwrapper* "$INITD"/S*SCWrapper* "$INITD"/S*sc-wrapper* "$INITD"/S*SC-Wrapper*; do
   [ -e "$svc" ] || continue
   [ -x "$svc" ] && "$svc" stop >/dev/null 2>&1 || true
   rm -f "$svc"
 done
}
clean_startup_lines(){
 mkdir -p "$(dirname "$USER_STARTUP")" 2>/dev/null || true
 [ -f "$USER_STARTUP" ] || printf '#!/bin/sh\n' >"$USER_STARTUP"
 tmp="$USER_STARTUP.tmp.$$"
 grep -vi -E 'sc-orbit|scwrapper|sc-wrapper' "$USER_STARTUP" >"$tmp" 2>/dev/null || printf '#!/bin/sh\n' >"$tmp"
 mv -f "$tmp" "$USER_STARTUP"; chmod 755 "$USER_STARTUP" 2>/dev/null || true
}
cleanup_legacy_extras(){
 rm -rf "$LEGACY_SETTINGS_DIR" "$APP/tester" "$FAT/_Utility/SC-Orbit" "$FAT/SC-Orbit-Recovery"
 rm -f "$SCRIPTS/SC-Orbit-Input-Tester.sh" \
       "$SCRIPTS/SC-Orbit Input Tester.sh" \
       "$SCRIPTS/SC-Orbit-Input-Lab.sh" \
       "$SCRIPTS/SC-Orbit Input Lab.sh" \
       "$FAT/SC-Orbit-Input-Tester.sh" \
       "$FAT/SC-Orbit Input Tester.sh" \
       "$FAT/SC-Orbit-Input-Lab.sh" \
       "$FAT/SC-Orbit Input Lab.sh" \
       "$FAT/_Utility/SC-Orbit_Input_Tester.mgl" \
       "$FAT/_Utility/SC-Orbit_Input_Lab.mgl" \
       "$FAT/_Utility/SC-Orbit_Input_Lab.rbf"
}
migrate_one(){
 src="$1"; dst="$2"
 [ -f "$src" ] || return 0
 if [ ! -f "$dst" ]; then
   mv -f "$src" "$dst" 2>/dev/null || { cp -f "$src" "$dst" 2>/dev/null && rm -f "$src"; }
 else
   rm -f "$src"
 fi
}
migrate_generated_files(){
 migrate_one "$OLD_CFG" "$CFG"
 migrate_one "$OLD_PROFILE" "$PROFILE"
 rm -f "$OLD_DIAG" "$OLD_TELEMETRY" "$OLD_SPINNER_TRACE" "$OLD_GYRO_TRACE" \
       "$APP/SC-Orbit-Diagnostic.txt" "$APP/SC-Orbit-Release-Telemetry.txt" \
       "$APP/SC-Orbit-Spinner-Trace.txt" "$APP/SC-Orbit-Gyro-Trace.txt" \
       "$FAT/SC-Orbit-Live-Proof.txt"
}
show_status(){
 echo "SC-Orbit current settings"
 echo "-------------------------"
 cat "$CFG" 2>/dev/null || echo "Defaults will be created when the driver starts."
 echo
 echo "Saved profile: /media/fat/linux/sc-orbit/sc-orbit-user-profile.ini"
 if [ -s "$PID" ]; then
   p="$(cat "$PID" 2>/dev/null)"
   [ -n "$p" ] && kill -0 "$p" 2>/dev/null && echo "Driver status: running (PID $p)" || echo "Driver status: stopped"
 else
   echo "Driver status: stopped"
 fi
 [ -n "$ROOT_PREFIX" ] || sleep 4
 queue_menu
 exit 0
}
restart_driver(){
 disable_legacy_services; stop_pidfile; stop_old_processes
 if [ "$NO_START" != "1" ]; then "$START" >/dev/null 2>&1 || finish "SC-Orbit failed to restart. Runtime log: /tmp/sc-orbit.log" 1; fi
 finish "SC-Orbit restarted." 0
}
reset_defaults(){ rm -f "$CFG"; restart_driver; }
stop_driver(){ stop_pidfile; stop_old_processes; finish "SC-Orbit stopped until restart or reboot." 0; }
uninstall(){
 stop_pidfile; stop_old_processes; clean_startup_lines
 cleanup_legacy_extras
 rm -rf "$APP"
 rm -f "$LOG" "$PID" "$SELF_DEST" \
       "$OLD_CFG" "$OLD_PROFILE" "$OLD_DIAG" "$OLD_TELEMETRY" \
       "$OLD_SPINNER_TRACE" "$OLD_GYRO_TRACE" "$FAT/SC-Orbit-Live-Proof.txt"
 finish "SC-Orbit removed. No recovery folder or SCWrapper backup is retained." 0
}

case "${1:-}" in
 --status) show_status ;;
 --restart) restart_driver ;;
 --reset) reset_defaults ;;
 --stop) stop_driver ;;
 --uninstall) uninstall ;;
esac

if [ -z "$ROOT_PREFIX" ]; then
 [ "$(id -u)" = "0" ] || finish "Run SC-Orbit from MiSTer's Scripts menu." 1
 [ -c /dev/uinput ] || finish "/dev/uinput is unavailable." 1
 case "$(uname -m 2>/dev/null)" in armv7l|armv7*|arm) ;; *) finish "This build requires MiSTer's ARMv7 Linux." 1;; esac
 mount -o remount,rw / >/dev/null 2>&1 || true
fi

mkdir -p "$APP" "$(dirname "$PID")" "$(dirname "$LOG")" "$(dirname "$USER_STARTUP")" || finish "SC-Orbit could not create its folders." 1
migrate_generated_files
cleanup_legacy_extras
rm -f "$SOURCE" "$APP/README.txt" "$APP/version.txt"

for svc in "$(rp /etc/init.d/S99sc-orbit)" "$(rp /etc/init.d/S99scwrapper-motion)"; do [ -x "$svc" ] && "$svc" stop >/dev/null 2>&1 || true; rm -f "$svc"; done
disable_legacy_services
[ -x "$STOP" ] && "$STOP" >/dev/null 2>&1 || true
stop_pidfile; stop_old_processes
rm -rf "$FAT/linux/sc-orbit-v2" "$FAT/linux/scwrapper-arcade"
rm -f "$FAT/sc-orbit-v3.cfg" "$FAT/scwrapper-motion.ini"

extract_block __SC_ORBIT_BINARY_BEGIN__ __SC_ORBIT_BINARY_END__ "$BIN" "$BIN_SIZE" "$BIN_SHA" || finish "Embedded ARM driver failed verification." 1
chmod 755 "$BIN"

cat >"$STOP" <<EOF_STOP
#!/bin/sh
PID="$PID"
if [ -s "\$PID" ]; then
 p="\$(cat "\$PID" 2>/dev/null)"
 [ -n "\$p" ] && kill "\$p" 2>/dev/null || true
 n=0
 while [ -n "\$p" ] && kill -0 "\$p" 2>/dev/null && [ "\$n" -lt 25 ]; do sleep 0.1; n=\$((n+1)); done
 [ -n "\$p" ] && kill -0 "\$p" 2>/dev/null && kill -9 "\$p" 2>/dev/null || true
fi
rm -f "\$PID"
EOF_STOP
chmod 755 "$STOP"

cat >"$START" <<EOF_START
#!/bin/sh
BIN="$BIN"
PID="$PID"
LOG="$LOG"
kill_legacy_scwrapper(){
 if command -v pidof >/dev/null 2>&1; then
   for name in scwrapper SCWrapper sc-wrapper SC-Wrapper; do
     for p in \$(pidof "\$name" 2>/dev/null); do kill "\$p" 2>/dev/null || true; done
   done
 fi
 for d in /proc/[0-9]*; do
   [ -d "\$d" ] || continue
   p="\${d#/proc/}"; [ "\$p" = "\$\$" ] && continue
   comm="\$(cat "\$d/comm" 2>/dev/null || true)"
   exe="\$(readlink "\$d/exe" 2>/dev/null || true)"
   cmd="\$(tr '\000' ' ' <"\$d/cmdline" 2>/dev/null || true)"
   sig="\$(printf '%s %s %s' "\$comm" "\$exe" "\$cmd" | tr '[:upper:]' '[:lower:]')"
   case "\$sig" in *scwrapper*|*sc-wrapper*) kill "\$p" 2>/dev/null || true ;; esac
 done
 sleep 1
}
kill_legacy_scwrapper
[ -x "\$BIN" ] || exit 1
if [ -s "\$PID" ]; then
 p="\$(cat "\$PID" 2>/dev/null)"
 [ -n "\$p" ] && kill -0 "\$p" 2>/dev/null && exit 0
 rm -f "\$PID"
fi
"\$BIN" >>"\$LOG" 2>&1 &
echo \$! >"\$PID"
sleep 1
p="\$(cat "\$PID" 2>/dev/null)"
[ -n "\$p" ] && kill -0 "\$p" 2>/dev/null
EOF_START
chmod 755 "$START"


if [ "$SELF" != "$SELF_DEST" ] && [ -f "$SELF" ]; then cp -f "$SELF" "$SELF_DEST" 2>/dev/null || true; chmod 755 "$SELF_DEST" 2>/dev/null || true; fi

clean_startup_lines
printf '%s >/dev/null 2>&1 &
' "$START" >>"$USER_STARTUP"
chmod 755 "$USER_STARTUP"

if [ "$NO_START" != "1" ]; then "$START" >/dev/null 2>&1 || finish "SC-Orbit installed but the driver did not remain running." 1; fi
finish "SC-Orbit installed." 0

